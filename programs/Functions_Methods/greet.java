// Define a public class named greet
public class greet {
    // The main method
    public static void main(String[] args) {
        greetings(); // Call the greetings method
    }
    // Define a static method called greetings
    static void greetings() {
        String greet_ = "Hello World!"; // Create a string variable named greet_ and assign it the value "Hello World!"
        System.out.println(greet_); // Print the value of greet_ to the console
    }
}
